package project;

public class AttributeGain {

	String attribute;
	double gain;
	
	public AttributeGain() {
		super();
	}
	public String getAttribute() {
		return attribute;
	}
	public void setAttribute(String attribute) {
		this.attribute = attribute;
	}
	public double getGain() {
		return gain;
	}
	public void setGain(double gain) {
		this.gain = gain;
	}
}
